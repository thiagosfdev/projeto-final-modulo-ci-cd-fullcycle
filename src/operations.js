function sum(a, b) {

  const n1 = a

  const n2 = b

  const result = n1 + n2

  return result

}

function sub(a, b) {

  const n1 = a

  const n2 = b

  const result = n1 - n2

  return result

}

function mult(a, b) {

  const n1 = a

  const n2 = b

  const result = n1 * n2

  return result

}

function div(a, b) {

  const n1 = a

  const n2 = b

  const result = n1 / n2

  return result

}

function mod(a, b) {

  const n1 = a

  const n2 = b

  const result = n1 % n2

  return result

}

module.exports = { div, mod, mult, sub, sum }
