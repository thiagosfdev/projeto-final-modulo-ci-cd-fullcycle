function sum(a, b) {

  const n1 = a

  const n2 = b

  return n1 + n2

}

function sub(a, b) {

  const n1 = a

  const n2 = b

  return n1 - n2

}

function mult(a, b) {

  const n1 = a

  const n2 = b

  return n1 * n2

}

function div(a, b) {

  const n1 = a

  const n2 = b

  return n1 / n2

}

function mod(a, b) {

  const n1 = a

  const n2 = b

  return n1 % n2

}

module.exports = { div, mod, mult, sub, sum }
